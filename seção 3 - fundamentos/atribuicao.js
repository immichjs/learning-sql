const x = 7
let y = 3

console.log(`${y} + ${x} = ${y += x}`) // Adição.
console.log(`${y} - 4 = ${y -= 4}`) // Subtração.
console.log(`${y} . 2 = ${y *= 2}`) // Múltiplicação.
console.log(`${y} / 2 = ${y / 2}`) // Divisão.
console.log(`${y} % 2 = ${y %= 2}`) // Modulação.

// Exponênciação.
let a = 5, b = 2
console.log(`${a}² = ${a **= b}`)